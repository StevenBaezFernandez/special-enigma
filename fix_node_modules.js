const fs = require('fs');
const path = require('path');

const libsRoot = path.join(__dirname, 'libs');
const nodeModulesRoot = path.join(__dirname, 'node_modules/@virteex');

if (!fs.existsSync(nodeModulesRoot)) {
  fs.mkdirSync(nodeModulesRoot, { recursive: true });
}

function linkLibs(currentPath) {
  const entries = fs.readdirSync(currentPath, { withFileTypes: true });
  for (const entry of entries) {
    const fullPath = path.join(currentPath, entry.name);
    if (entry.isDirectory()) {
      if (fs.existsSync(path.join(fullPath, 'package.json'))) {
        const pkg = JSON.parse(fs.readFileSync(path.join(fullPath, 'package.json'), 'utf8'));
        if (pkg.name && pkg.name.startsWith('@virteex/')) {
          const targetName = pkg.name.replace('@virteex/', '');
          const linkPath = path.join(nodeModulesRoot, targetName);
          if (fs.existsSync(linkPath)) {
            fs.unlinkSync(linkPath);
          }
          // Point to dist for production-ready linking
          const distPath = path.join(__dirname, 'dist', path.relative(__dirname, fullPath));
          if (!fs.existsSync(distPath)) {
             fs.mkdirSync(distPath, { recursive: true });
          }
          console.log(`Linking ${pkg.name} to ${distPath}`);
          fs.symlinkSync(distPath, linkPath, 'junction');
        }
      } else {
        linkLibs(fullPath);
      }
    }
  }
}

linkLibs(libsRoot);
